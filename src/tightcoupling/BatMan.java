package tightcoupling;
public class BatMan {
	void up() {
		System.out.println("BatMan move up");
	}
	void down() {
		System.out.println("BatMan move down");
	}
	void right() {
		System.out.println("BatMan move right");
	}
	void left() {
		System.out.println("BatMan move left");
	}
}
