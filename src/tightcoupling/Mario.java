package tightcoupling;
public class Mario {
	public void up() {
		System.out.println("Mario move up");
	}
	public void down() {
		System.out.println("Mario move down");
	}
	public void right() {
		System.out.println("Mario move right");
	}
	public void left() {
		System.out.println("Mario move left");
	}
}
